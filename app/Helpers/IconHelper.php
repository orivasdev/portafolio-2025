    /**
     * Obtener el nombre del icono por defecto
     */
    public static function getDefaultIcon(): string
    {
        return 'wrench';
    }

    /**
     * Verificar si un icono existe
     */
    public static function iconExists(string $iconName): bool
    {
        return array_key_exists($iconName, self::getCategoryIcons());
    }

    /**
     * Obtener el emoji correspondiente al icono
     */
    public static function getIconEmoji(string $iconName): string
    {
        $icons = self::getCategoryIcons();
        return $icons[$iconName] ?? '🔧';
    }

    /**
     * Obtener solo el emoji (sin el nombre)
     */
    public static function getIconEmojiOnly(string $iconName): string
    {
        $emoji = self::getIconEmoji($iconName);
        return explode(' ', $emoji)[0] ?? '🔧';
    }

    /**
     * Obtener solo el nombre del icono (sin el emoji)
     */
    public static function getIconNameOnly(string $iconName): string
    {
        $emoji = self::getIconEmoji($iconName);
        $parts = explode(' ', $emoji, 2);
        return $parts[1] ?? $iconName;
    }
